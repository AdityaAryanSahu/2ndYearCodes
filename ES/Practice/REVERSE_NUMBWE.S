    LDR R0,=SRC
	MOV R1,#0XF
	MOV R5,#3
	LDR R10,=DST
LOOP1
     LDR R2,[R0]
	 ADD R0,#4
	 MOV R4,#0
LOOP2
	 AND R6,R2,R1
	 ORR R4,R6
	 LSR R2,#4

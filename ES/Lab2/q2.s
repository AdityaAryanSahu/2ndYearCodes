	AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0X10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	MOV R0,#0
	MOV R1,#1
	MOV R2,#10;;NUMBERS REQUIRED APART FROM 0 AND 1
	LDR R3, =DST
	STR R0,[R3]
	ADD R3,R3,#4
	STR R1,[R3]
LOOP ADD R6,R0,R1
     MOV R0,R1
	 MOV R1,R6
	ADD R3,R3,#4
	STR R6,[R3]
	SUBS R2,R2,#1
	BEQ STOP ;stop if Z=1;
	B LOOP
STOP
	B STOP
	AREA MTCODE,DATA,READWRITE
DST DCD 0
	END